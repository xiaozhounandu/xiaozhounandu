-- 为大约60%的客户生成跟进记录
INSERT INTO `follow_up` (`customer_id`, `follower_id`, `type`, `content`, `result`, `next_follow_time`, `create_time`)
SELECT
    customer_id,
    owner_id as follower_id,
    CASE seq.rnd % 5
        WHEN 0 THEN 'CALL'
        WHEN 1 THEN 'EMAIL'
        WHEN 2 THEN 'MEETING'
        WHEN 3 THEN 'WECHAT'
        ELSE 'OTHER'
    END as type,

    CASE seq.rnd % 8
        WHEN 0 THEN '电话沟通产品需求，客户表示很感兴趣'
        WHEN 1 THEN '发送产品资料和报价单'
        WHEN 2 THEN '上门拜访，进行产品演示'
        WHEN 3 THEN '技术方案讨论，解决客户疑问'
        WHEN 4 THEN '商务谈判，讨论合作细节'
        WHEN 5 THEN '售后服务，了解客户使用情况'
        WHEN 6 THEN '定期回访，维护客户关系'
        ELSE '跟进合同签署事宜'
    END as content,

    CASE seq.rnd % 6
        WHEN 0 THEN '客户表示很有兴趣，约定下次沟通时间'
        WHEN 1 THEN '等待客户内部讨论和决策'
        WHEN 2 THEN '技术方案认可，进入商务谈判阶段'
        WHEN 3 THEN '合同已签订，准备实施阶段'
        WHEN 4 THEN '客户满意，继续深化合作'
        ELSE '需要进一步技术细节确认'
    END as result,

    DATE_ADD(create_time, INTERVAL (seq.rnd % 30 + 7) DAY) as next_follow_time,

    DATE_ADD(create_time, INTERVAL (seq.rnd % 15) DAY) as create_time

FROM (
    SELECT
        id as customer_id,
        owner_id,
        create_time,
        FLOOR(RAND() * 1000) as rnd
    FROM customer
    WHERE id > 4
    AND FLOOR(RAND() * 10) < 6  -- 60%的客户有跟进记录
) seq;


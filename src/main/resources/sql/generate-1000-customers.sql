ORDER BY level;

SELECT '=== 行业分布TOP 10 ===' as 统计信息;
SELECT
    industry as 行业,
    COUNT(*) as 客户数,
    ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM customer), 2) as 占比百分比
FROM customer
WHERE industry IS NOT NULL AND industry != ''
GROUP BY industry
ORDER BY COUNT(*) DESC
LIMIT 10;

